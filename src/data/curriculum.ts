export const getItemById = (id: string, source: LearningItem[] = curriculum) => source.find((item) => item.id === id)

const slugify = (text: string) => text.trim().toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '')

export const normalizeCustomCurriculum = (rows: Partial<LearningItem>[]): LearningItem[] => rows
  .filter((row) => row.text && row.meaning)
  .map((row) => {
    const level: LearningItem['level'] = row.level === 'a1' ? 'a1' : 'pre-a1'
    const type: LearningItem['type'] = row.type === 'phrase' ? 'phrase' : 'word'
    const theme = row.theme || 'custom'
    const text = row.text?.trim() ?? ''

    return {
      id: row.id || `custom-${slugify(text)}`,
      level,
      theme,
      unit: row.unit || `custom-${theme}`,
      type,
      text,
      meaning: row.meaning?.trim() ?? '',
      emoji: row.emoji || '⭐',
      prompt: row.prompt || `Say ${text}.`,
      tags: row.tags?.length ? row.tags : [theme, level, 'custom'],
      ageRange: row.ageRange || [6, 8],
      difficulty: row.difficulty || (level === 'pre-a1' ? 1 : 2),
      priority: row.priority ?? 80,
      source: 'custom' as const,
      reviewIntervalDays: row.reviewIntervalDays || 2,
      audioSrc: row.audioSrc,
      imageSrc: row.imageSrc,
    }
  })
