const enrichCoreItem = (item: CurriculumSeed): LearningItem => ({
  ...item,
  unit: item.unit ?? `${item.level}-${item.theme}-core`,
  ageRange: item.ageRange ?? (item.level === 'pre-a1' ? [6, 7] : [7, 8]),
  difficulty: item.difficulty ?? (item.level === 'pre-a1' ? 1 : 2),
  priority: item.priority ?? 90,
  source: item.source ?? 'core',
  reviewIntervalDays: item.reviewIntervalDays ?? 2,
})

export const builtInCurriculum: LearningItem[] = [...coreCurriculum.map(enrichCoreItem), ...extraCurriculum]

export const curriculum: LearningItem[] = builtInCurriculum

export const mergeCurriculum = (customCurriculum: LearningItem[] = []) => {
  const byId = new Map<string, LearningItem>()
  builtInCurriculum.forEach((item) => byId.set(item.id, item))
  customCurriculum.forEach((item) => byId.set(item.id, { ...item, source: 'custom' }))
  return Array.from(byId.values())
}

